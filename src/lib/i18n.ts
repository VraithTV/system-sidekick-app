/**
 * Get a translated string. Falls back to English if key is missing.
 * Supports {variable} interpolation.
 */
export function t(lang: string, key: string, vars?: Record<string, string>): string {
  const dict = translations[lang] || translations.en;
  let str = dict[key] || translations.en[key] || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    }
  }
  return str;
}

/**
 * Hook-friendly helper: returns a `t` function bound to the current language.
 */
export function createT(lang: string) {
  return (key: string, vars?: Record<string, string>) => t(lang, key, vars);
}
